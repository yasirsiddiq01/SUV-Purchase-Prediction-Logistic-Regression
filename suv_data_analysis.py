import pandas as pd
import numpy as np

np.random.seed(42)

# Generate 400 samples
n = 400

# Realistic ranges
ages = np.random.randint(18, 70, n)
salaries = np.random.randint(20000, 150000, n)

# Purchase logic: higher age + higher salary = more likely to purchase
# This creates a realistic decision boundary
purchase_prob = 1 / (1 + np.exp(-((ages - 45) / 12 + (salaries - 90000) / 40000)))
purchased = (np.random.random(n) < purchase_prob).astype(int)

# Ensure at least 25% purchase rate
if purchased.sum() < n * 0.25:
    # Adjust to ensure enough positive examples
    needed = int(n * 0.25) - purchased.sum()
    indices = np.where(purchased == 0)[0][:needed]
    purchased[indices] = 1

# Create DataFrame
df = pd.DataFrame({
    'User ID': range(1, n + 1),
    'Gender': np.random.choice(['Male', 'Female'], n, p=[0.6, 0.4]),
    'Age': ages,
    'EstimatedSalary': salaries,
    'Purchased': purchased
})

# Save to CSV
df.to_csv('SUV_Purchase.csv', index=False)
print(f"Dataset created with {n} samples")
print(f"Purchase rate: {df['Purchased'].mean()*100:.1f}%")
